// Type for the dictionary
type Dict = typeof T.tr;
type Key = keyof Dict;

const LangCtx = createContext<{
  lang: Lang;
  t: Dict;
  setLang: (l: Lang) => void;
  fmt: (key: Key) => string;
}>({
  lang: 'tr', t: T.tr, setLang: () => {}, fmt: (k) => String(k),
});

/**
 * Detect language from various sources, in priority order:
 *  1. Saved cookie/localStorage (user has manually chosen)
 *  2. Geo-IP country (set by middleware in cookie `sm_country`)
 *  3. Browser navigator.language
 *  4. Default: Turkish (primary site language)
 */
function detectLang(): Lang {
  if (typeof window === 'undefined') return 'tr';

  // 1) Saved preference
  try {
    const saved = localStorage.getItem('sm_lang') as Lang | null;
    if (saved === 'tr' || saved === 'en' || saved === 'de') return saved;
  } catch {}

  // 2) Geo-IP cookie set by middleware
  try {
    const cookies = document.cookie.split(';').map(c => c.trim());
    const country = cookies.find(c => c.startsWith('sm_country='))?.split('=')[1]?.toUpperCase();
    if (country) {
      if (country === 'TR') return 'tr';
      if (['DE', 'AT', 'CH', 'LI'].includes(country)) return 'de';
      return 'en';
    }
  } catch {}

  // 3) Browser language
  try {
    const browser = navigator.language?.toLowerCase() || '';
    if (browser.startsWith('de')) return 'de';
    if (browser.startsWith('tr')) return 'tr';
    if (browser.startsWith('en')) return 'en';
  } catch {}

  return 'tr';
}

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('tr');

  useEffect(() => {
    const detected = detectLang();
    setLangState(detected);
    if (typeof document !== 'undefined') document.documentElement.lang = detected;
  }, []);

  function setLang(l: Lang) {
    setLangState(l);
    if (typeof window !== 'undefined') {
      try { localStorage.setItem('sm_lang', l); } catch {}
      document.cookie = `sm_lang=${l}; path=/; max-age=${60 * 60 * 24 * 365}; SameSite=Lax`;
    }
    if (typeof document !== 'undefined') document.documentElement.lang = l;
  }

  const dict = T[lang];

  function fmt(key: Key): string {
    return dict[key] || T.tr[key] || String(key);
  }

  return (
    <LangCtx.Provider value={{ lang, t: dict, setLang, fmt }}>
      {children}
    </LangCtx.Provider>
  );
}

export function useLang() { return useContext(LangCtx); }

/**
 * Helper: format a date for the current locale.
 * Date is a Date | string | number.
 */
export function formatDate(date: Date | string | number, lang: Lang, opts?: Intl.DateTimeFormatOptions): string {
  if (!date) return '';
  const d = typeof date === 'object' ? date : new Date(date);
  if (isNaN(d.getTime())) return '';
  const locale = lang === 'tr' ? 'tr-TR' : lang === 'de' ? 'de-DE' : 'en-US';
  return d.toLocaleString(locale, opts || { day: 'numeric', month: 'short', year: 'numeric' });
}

/**
 * Helper: format a number for the current locale.
 */
export function formatNumber(n: number, lang: Lang): string {
  const locale = lang === 'tr' ? 'tr-TR' : lang === 'de' ? 'de-DE' : 'en-US';
  return n.toLocaleString(locale);
}

/**
 * Translate a backend error into the user's language.
 * Backend errors carry a `code` field (e.g. WRONG_CREDENTIALS).
 * If we have a translation for `err_<CODE>`, use it; otherwise fall back
 * to the raw backend message (Turkish) so nothing is ever silently lost.
 */
export function translateError(e: any, dict: any): string {
  if (e?.code && dict[`err_${e.code}`]) return dict[`err_${e.code}`];
  return e?.message || dict.err_GENERIC || 'Error';
}
